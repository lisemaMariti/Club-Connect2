    private void btnChangePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangePasswordActionPerformed
        String email = txtEmail.getText().trim();
    String token = txtToken.getText().trim(); // New token field
    String newPassword = new String(txtNewPassword.getPassword()).trim();
    String confirmPassword = new String(txtConfirmPassword.getPassword()).trim();

    // Basic validation
    if (email.isEmpty() || token.isEmpty() || newPassword.isEmpty() || confirmPassword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields including the reset token.", "Validation Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (!newPassword.equals(confirmPassword)) {
        JOptionPane.showMessageDialog(this, "Passwords do not match.", "Validation Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Password strength validation
    if (newPassword.length() < 6) {
        JOptionPane.showMessageDialog(this, "Password must be at least 6 characters.", "Validation Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        // First, get user ID from email
        int userId = UserDAO.getUserIdByEmail(email);
        if (userId == -1) {
            JOptionPane.showMessageDialog(this, "No account found with this email address.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Verify the reset token
        boolean isValidToken = UserDAO.verifyResetToken(userId, token);
        if (!isValidToken) {
            JOptionPane.showMessageDialog(this, "Invalid or expired reset token. Please request a new one.", "Token Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Hash the new password
        String hashedPassword = PasswordUtil.hashPassword(newPassword);

        // Update password in DB
        boolean success = UserDAO.updatePasswordByEmail(email, hashedPassword);
        if (success) {
            // Mark token as used to prevent reuse
            UserDAO.markTokenAsUsed(userId);
            
            JOptionPane.showMessageDialog(this, "Password changed successfully!");
            // Clear fields
            txtEmail.setText("");
            txtToken.setText("");
            txtNewPassword.setText("");
            txtConfirmPassword.setText("");
              new clubconnect.ui.LoginForm().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Failed to change password. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }





    }//GEN-LAST:event_btnChangePasswordActionPerformed

